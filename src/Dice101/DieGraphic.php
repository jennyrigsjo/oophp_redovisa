namespace Anri16\Dice101;

/**
 * A supporting class for the game 'Dice 100'.
 */
class DieGraphic extends Die1
{

    /**
     * Constructor to initiate a single die.
     */
    public function __construct()
    {
        parent::__construct();
    }

    /**
     * Roll the die and remember the resulting value.
     *
     */
    public function roll()
    {
        parent::roll();
        $this->serie[] = $this->getLastRoll();
    }

    /**
     * Get a graphic representation of the value generated by rolling the die.
     *
     * @return string as a class name representing the graphic version of the value.
     */
    public function graphic()
    {
        $value = $this->getLastRoll();
        $className = "die-" . $value;
        return $className;
    }
}
